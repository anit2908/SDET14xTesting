package ex_02_Operators;

public class Logical {

    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;
        boolean c = a && b;
        boolean d = a || b;
        boolean e = !a;

        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
